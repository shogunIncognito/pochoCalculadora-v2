    private void borrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_borrarMouseClicked
        String text = this.respuesta.getText();
        
        if (text.length() > 0) {
            this.respuesta.setText(text.substring(0, text.length() - 1));
        }
    }//GEN-LAST:event_borrarMouseClicked

    private void raizMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_raizMouseClicked
        if (this.respuesta.getText().length() > 0) {
            this.raizNum = Double.parseDouble(this.respuesta.getText());
            String res = Double.toString(Math.sqrt(this.raizNum));

            this.topText.setText("Raiz cuadrada");

            if (this.raizNum % 1 == 0) {
                this.respuesta.setText(res.substring(0, res.length() - 2));
            } else {
                this.respuesta.setText(res);
            }
        } else {
            this.respuesta.setText("Vacio mi bro");
        }               
    }//GEN-LAST:event_raizMouseClicked

